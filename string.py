#uppercase
a = "kiet"
print(a.upper())

#lowercase
a = "KIET"
print(a.lower())

#remove space
a = "kiet"
print(a.replace('t','k'))

# slicing
#syntax : string[start:end:step]
#slicing
s="kiet students"
print(s[0:5])
print(s[1::])
print(s[:5:])
print(s[::2])
print(s[1:9:2])

#reverse
s="python"
print(s[-1::])
print(s[:-3:])
print(s[::-1])
print(s[-1:-5:-1])
 
s="kietwcollegestudents"
print(s[::-1])

